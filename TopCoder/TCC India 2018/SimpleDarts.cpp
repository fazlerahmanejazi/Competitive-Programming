using namespace std;

class SimpleDarts {
    public:
    int highestScore(int F) {
        vector<int> a ;
        for(int i=1 ; i<=F ; i++) a.push_back(i) ;
        for(int j=2 ; j<=2*F ; j+=2) a.push_back(j) ;
        for(int k=3 ; k<=3*F ; k+=3) a.push_back(k) ;
        a.push_back(25) ;
        a.push_back(50) ;
        sort(a.begin(), a.end()) ;
        reverse(a.begin(), a.end()) ;
        return a[0]+a[1]+a[2] ;
    }
};

